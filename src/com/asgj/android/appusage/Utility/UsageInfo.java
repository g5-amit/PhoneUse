package com.asgj.android.appusage.Utility;

public class UsageInfo {

	long mIntervalStartTime;
	long mIntervalEndTime;
	long mIntervalDuration;

	public long getmIntervalStartTime() {
		return mIntervalStartTime;
	}

	public void setmIntervalStartTime(long mIntervalStartTime) {
		this.mIntervalStartTime = mIntervalStartTime;
	}

	public long getmIntervalEndTime() {
		return mIntervalEndTime;
	}

	public void setmIntervalEndTime(long mIntervalEndTime) {
		this.mIntervalEndTime = mIntervalEndTime;
	}

	public long getmIntervalDuration() {
		return mIntervalDuration;
	}

	public void setmIntervalDuration(long mIntervalDuration) {
		this.mIntervalDuration = mIntervalDuration;
	}
}
